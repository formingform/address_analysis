    CREATE TABLE IF NOT EXISTS `address_type`  (
         `address` varchar(42) NOT NULL COMMENT '地址',
         `contract` boolean NOT NULL DEFAULT 0 COMMENT '0:EOA地址；1:合约地址',
         PRIMARY KEY (`address`)
    ) ENGINE = InnoDB  COMMENT = '地址类型表';

    DROP TABLE IF EXISTS `transfer_item`;
    CREATE TABLE `transfer_item`  (
        `id` bigint(0) NOT NULL AUTO_INCREMENT COMMENT 'id',
        `block_number` bigint(0) NULL DEFAULT NULL,
        `block_timestamp` datetime(0) NULL DEFAULT NULL COMMENT '区块时间',
        `tx_hash` varchar(128) NULL DEFAULT NULL COMMENT '交易哈希',
        `tx_from` varchar(128) NULL DEFAULT NULL COMMENT '转账发起者',
        `tx_to` varchar(128) DEFAULT NULL COMMENT '转账接收者',
        `value` varchar(64) DEFAULT NULL COMMENT '交易金额',
        `create_time` timestamp(0) NOT NULL DEFAULT CURRENT_TIMESTAMP(0) COMMENT '创建时间',
        `update_time` timestamp(0) NOT NULL DEFAULT CURRENT_TIMESTAMP(0) ON UPDATE CURRENT_TIMESTAMP(0) COMMENT '更新时间',
        PRIMARY KEY (`id`) USING BTREE,
        INDEX `block_number`(`block_number`) USING BTREE
    ) ENGINE = InnoDB  COMMENT = '转移记录' auto_increment = 1;

